        //ExportNCPDP/1
        [HttpGet("{id}")]
        public List<string> ExportNCPDPFiles(long id)
        {
            List<string> result = new List<string>();
            _logger.Log(LogLevel.Information, "Generate NCPDP Files");
            IConfigurationRoot configuration = new ConfigurationBuilder().SetBasePath(Directory.GetCurrentDirectory()).AddJsonFile("appsettings.json").Build();
            string DestinationFolder = configuration["NCPDPDestinationFolder"];
            int goodDatabaseRecords = 0;
            string ProductionFlag = "P";
            if (id != 1) ProductionFlag = "T";
            StringBuilder sb = new StringBuilder();
            int sequenceNumber = 1;
            foreach (HisHeader header in _context.Headers.Where(x=>x.CountyCode=="305"))
            {
                sb.Clear();
                sb.AppendLine(Pah42Header(header, ProductionFlag));
                foreach (HisDetail detail in _context.Details.Where(x => x.FileID == header.FileID)) 
                {
                    sb.AppendLine(Pah42Detail(detail));
                    foreach (HisCompound1 cd1 in _context.Compound1s.Where(x => x.DetailID == detail.DetailID)) 
                    {
                        sb.AppendLine(Pah42Cd1(cd1));
                    }
                    foreach (HisCompound2 cd2 in _context.Compound2s.Where(x => x.DetailID == detail.DetailID)) 
                    {
                        sb.AppendLine(Pah42Cd2(cd2));
                    }
                    goodDatabaseRecords++;
                }
                sb.AppendLine(Pah42Trailer(header));
                System.IO.File.WriteAllText(Path.Combine(DestinationFolder, "IEHP_" + header.CountyCode + "_P42_MCE_" + DateTime.Today.ToString("yyyyMMdd") + "_" + sequenceNumber.ToString().PadLeft(5,'0') + ".dat"), sb.ToString());
                sequenceNumber++;
            }
            sequenceNumber = 1;
            foreach (HisHeader header in _context.Headers.Where(x => x.CountyCode == "306"))
            {
                sb.Clear();
                sb.AppendLine(Pah42Header(header, ProductionFlag));
                foreach (HisDetail detail in _context.Details.Where(x => x.FileID == header.FileID))
                {
                    sb.AppendLine(Pah42Detail(detail));
                    foreach (HisCompound1 cd1 in _context.Compound1s.Where(x => x.DetailID == detail.DetailID))
                    {
                        sb.AppendLine(Pah42Cd1(cd1));
                    }
                    foreach (HisCompound2 cd2 in _context.Compound2s.Where(x => x.DetailID == detail.DetailID))
                    {
                        sb.AppendLine(Pah42Cd2(cd2));
                    }
                    goodDatabaseRecords++;
                }
                sb.AppendLine(Pah42Trailer(header));
                System.IO.File.WriteAllText(Path.Combine(DestinationFolder, "IEHP_" + header.CountyCode + "_P42_MCE_" + DateTime.Today.ToString("yyyyMMdd") + "_" + sequenceNumber.ToString().PadLeft(5, '0') + ".dat"), sb.ToString());
                sequenceNumber++;
            }

            result.Add(_context.Details.Count().ToString());
            result.Add(goodDatabaseRecords.ToString());
            return result;
        }
