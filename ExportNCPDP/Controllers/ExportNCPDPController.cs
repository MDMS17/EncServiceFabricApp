            sb.Append(header.BatchNumber.PadRight(7,' '));
            sb.Append(header.CreationDate);
            sb.Append(header.CreationTime);
            sb.Append(header.ReceiverID.PadRight(24, ' '));
            sb.Append(header.ReportStartDate);
            sb.Append(header.ReportEndDate);
            sb.Append(ProductionFlag);
            sb.Append(header.TransmissionAction);
            sb.Append(header.SubmissionNumber);
            sb.Append("".PadRight(3609, ' '));
            return sb.ToString();
        }
